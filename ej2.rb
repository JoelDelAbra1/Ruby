# Definir variables
x = 5
y = 2.5
z = 7.8

# Realizar operaciones
sum = x + y # suma un entero y un flotante
dif = z - x # resta un flotante y un entero
mult = y * z # multiplica dos flotantes
div = z / y # divide un flotante por otro

# Mostrar resultados
puts "La suma de #{x} y #{y} es #{sum}."
puts "La diferencia entre #{z} y #{x} es #{dif}."
puts "El producto de #{y} y #{z} es #{mult}."
puts "El cociente de #{z} dividido por #{y} es #{div}."